@dataclass(frozen=True)
class RangeRule:
    start: float
    end: float
    value: Any


def lookup(code: int | str, mapping: dict[Any, Any], default: Any = None) -> Any:
    return mapping.get(code, default)


def lookup_range(value: float, rules: list[RangeRule], default: Any = None) -> Any:
    for rule in rules:
        if rule.start <= value <= rule.end:
            return rule.value
    return default


